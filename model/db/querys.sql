CREATE TABLE USER(
    ID INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
    NOME VARCHAR(255) NOT NULL, 
    EMAIL VARCHAR(255) NOT NULL UNIQUE,
    SENHA VARCHAR(255) NOT NULL
)



CREATE TABLE CABELOS(
    ID INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
    NOME_IMAGE VARCHAR(255) NOT NULL, 
    PRECO REAL VARCHAR(255) NOT NULL, 
    IMAGEM_URL VARCHAR(255) NOT NULL
)




CREATE TABLE HISTORICOPAGAMENTO (
    ID_TABLE INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
    ID_USER INTEGER NOT NULL,
    ID_CABELO INTEGER NOT NULL,
    DATA_TRANSACAO DATETIME DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_ID_USER FOREIGN KEY (ID_USER) REFERENCES USER (ID),
    CONSTRAINT FK_ID_CABELO FOREIGN KEY (ID_CABELO) REFERENCES CABELOS (ID)
);



CREATE TABLE AGENDAMENTOS(
    IDUSER VARCHAR(255) NOT NULL,
    ID_CABELO VARCHAR(255) NOT NULL, 
    DATA_TRANSACAO DATETIME DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_ID_USER FOREIGN KEY (IDUSER) REFERENCES USER (ID),
    CONSTRAINT FK_ID_CABELO FOREIGN KEY (ID_CABELO) REFERENCES CABELOS (ID)
)